const iconMap = {
  truck: TruckIcon,
  rocket: RocketIcon,
  basket: BasketIcon,
};

export default async function PromoBanner() {
  const settings = await getBannerSettings();
  if (!settings || !settings.enabled) return null;

  return (
    <div className="w-full bg-orange-500 text-white">
      <div className="max-w-7xl mx-auto px-4">
        <div className="flex items-center justify-center gap-6 md:gap-12 py-2.5 overflow-x-auto scrollbar-hide">
          {settings.items.map((item, i) => {
            const Icon = iconMap[item.icon] ?? TruckIcon;
            return (
              <div key={i} className="flex items-center gap-2 whitespace-nowrap flex-shrink-0">
                <Icon />
                <span className="text-sm font-semibold">{item.text}</span>
              </div>
            );
          })}
        </div>
      </div>
    </div>
  );
}
